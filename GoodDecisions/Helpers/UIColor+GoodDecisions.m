#import "UIColor+GoodDecisions.h"

@implementation UIColor (GoodDecisions)

+(UIColor *)darkOrangeColor{
    return [UIColor colorWithRed:1.f green:87./255.f blue:59./255.f alpha:1];
}

+(UIColor *)lightOrangeColor{
    return [UIColor colorWithRed:1.f green:144./255.f blue:126./255.f alpha:1];
}

+(UIColor *)tealColor{
    return [UIColor colorWithRed:3./255.f green:161./255.f blue:171./255.f alpha:1];
}
@end
